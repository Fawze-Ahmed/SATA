const root = document.documentElement;
window.addEventListener("pointermove", (event) => {
  root.style.setProperty("--mx", `${event.clientX}px`);
  root.style.setProperty("--my", `${event.clientY}px`);
});

const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("is-visible");
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.16 });

function wireInteractions() {
  document.querySelectorAll(".section, .metrics, .service-card, .tech-card, .project-card, .suite-card, .why-grid article").forEach((el) => {
    if (el.dataset.wiredReveal === "true") return;
    el.dataset.wiredReveal = "true";
    el.classList.add("reveal");
    revealObserver.observe(el);
  });

  if (!matchMedia("(pointer: fine)").matches) return;
  document.querySelectorAll(".service-card, .tech-card, .project-card").forEach((card) => {
    if (card.dataset.wiredTilt === "true") return;
    card.dataset.wiredTilt = "true";
    card.addEventListener("pointermove", (event) => {
      const rect = card.getBoundingClientRect();
      const x = ((event.clientX - rect.left) / rect.width - 0.5) * 8;
      const y = ((event.clientY - rect.top) / rect.height - 0.5) * -8;
      card.style.setProperty("--tilt-x", `${y}deg`);
      card.style.setProperty("--tilt-y", `${x}deg`);
    });
    card.addEventListener("pointerleave", () => {
      card.style.setProperty("--tilt-x", "0deg");
      card.style.setProperty("--tilt-y", "0deg");
    });
  });
}

applyLang(new URLSearchParams(location.search).get("lang") === "en" ? "en" : "ar");
wireInteractions();
