  document.querySelector("[data-lang-toggle]").textContent = lang === "ar" ? "EN" : "AR";
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = content[lang][el.dataset.i18n] || el.textContent;
  });
